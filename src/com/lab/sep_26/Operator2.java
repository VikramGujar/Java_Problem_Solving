package com.lab.sep_26;

public class Operator2 {

	 public static void main(String args[])
	    {
	        double a = 1 + 9;	// 10.00 result in double 
	        double b = a / 6;	// 10.00/6 = 1.6
	        int c = 1 + 9;		// 10 result in int 
	        int d = c / 8;		// 1
	        System.out.print(b + " , " + d);	// 1.6 , 1

	    } 
}
