package com.lab.sep_26;

public class Operator4 {

	  public static void main(String[] args) 
	  {
	    
	    int a = 21;
	    int b = a;
	    System.out.println(b);	// 21

	    b += a;	// 42
	    System.out.println(b);	// 42
	    
	    a *= a;	// 21 * 21  = 441
	    System.out.println(a);	// 441
	  }
	

}
