package com.lab.sep_26;

public class Operator3 {

	 public static void main(String args[]) 
	    {    
	         double a = 892.457; 
	         int  b = 10;
	         a = a % 10;	// 892.457 % 10 = 2.457
	         b = b % 10;	// 10 % 10 = 0
	         System.out.println(a + " , "  + b);	//2.457 , 0 
	    } 
}
